async function seed() {
  console.log('🌱 Seeding listings...\n')

  const existing = await client.fetch(`count(*[_type == "listing"])`)
  if (existing > 0) {
    console.log(`⚠️  Found ${existing} existing listings — deleting them first...`)
    const ids = await client.fetch(`*[_type == "listing"]._id`)
    for (const id of ids) {
      await client.delete(id)
    }
    console.log('   ✓ Deleted\n')
  }

  console.log(`📦 Creating ${listings.length} listings...`)
  for (const listing of listings) {
    const result = await client.create(listing)
    console.log(`   ✓ ${listing.title} (${result._id})`)
  }

  console.log(`\n✅ Done! ${listings.length} listings added to Sanity.`)
}

seed().catch((err) => {
  console.error('❌ Failed:', err.message)
  process.exit(1)
})
