async function seedFaqs() {
  const neighborhoods = await client.fetch('*[_type == "neighborhood"] { _id, slug }')

  let updated = 0
  for (const n of neighborhoods) {
    const slug = n.slug?.current
    const faqs = faqsBySlug[slug]
    if (!faqs) {
      console.log(`⚠️  No FAQ data for slug: ${slug}`)
      continue
    }

    const faqWithKeys = faqs.map((f, i) => ({
      _type: 'faqItem',
      _key: `faq-${i}`,
      question: f.question,
      answer: f.answer,
    }))

    await client.patch(n._id).set({ faq: faqWithKeys }).commit()
    console.log(`✅ Updated FAQ for: ${slug} (${faqs.length} questions)`)
    updated++
  }

  console.log(`\nDone — updated ${updated}/${neighborhoods.length} neighborhoods.`)
}

seedFaqs().catch((err) => {
  console.error(err)
  process.exit(1)
})
